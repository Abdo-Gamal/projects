	public static boolean iSnum(String x) {
		if (x.charAt(0) == '0') {
			return false;
		}
		for (int i = 0; i < x.length(); i++) {
			if (! ('0' <= x.charAt(i) && x.charAt(i) <= '9') ) {
				return false;
			}
		}
		return true;
	}

